- ‘회사’는 이용자의 요청에 의해 해지 또는 삭제된 개인정보는 ‘회사’가 수집하는 개인정보의 보유 및 이용기간에 명시된 바에 따라 처리하고 그 외의 용도로 열람 또는 이용할 수 없도록 처리하고 있습니다. 


